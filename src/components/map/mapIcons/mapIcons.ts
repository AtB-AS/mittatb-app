/**
 * Returns MapImageSources with keys for both toggled and untoggled.
 * Toggling between them is how we can refresh map icons, which is
 * needed when using <MapboxGL.Images />.
 * Since the images are the same, it is only noticable for the user
 * when missing images appear.
 */
const getToggleableImageSources = (imageSourcesArray: MapImageSources[]) => {
  const toggleableImageSources: MapImageSources = {};

  imageSourcesArray.forEach((imageObjects: MapImageSources) => {
    Object.keys(imageObjects).forEach((iconName) => {
      toggleableImageSources[`${iconName}${MAP_ICONS_UNTOGGLED_SUFFIX}`] =
        imageObjects[iconName];
      toggleableImageSources[`${iconName}${MAP_ICONS_TOGGLED_SUFFIX}`] =
        imageObjects[iconName];
    });
  });

  return toggleableImageSources;
};

