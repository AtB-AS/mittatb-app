    circleRadius: paint['circle-radius'] || 1, // Mapping circle-radius
    circleColor: paint['circle-color'] || '#000', // Mapping circle-color
    circleStrokeColor: paint['circle-stroke-color'] || '#000', // Mapping circle-stroke-color
    circleStrokeWidth: paint['circle-stroke-width'] || 0, // Mapping circle-stroke-width
