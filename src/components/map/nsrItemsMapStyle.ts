export const mapStyleItemToSymbolStyle = (
  nsrStyleItem: (typeof nsrStyleItems)[0],
  selectedFeatureId?: string,
) => {
  const layout = nsrStyleItem.layout || {};
  const paint = nsrStyleItem.paint || {};

  const featureId = ['get', 'id'];
  const isSelected = ['==', featureId, selectedFeatureId];

  return {
    iconImage: [
      'case',
      isSelected,
      layout['icon-image'] + 'Selected' + '_local',
      layout['icon-image'] + '_local' || '',
    ],
    // iconImage: [
    //   'concat',
    //   layout['icon-image'],
    //   ['case', isSelected, 'Selected_local', '_local'],
    // ],
    iconSize: 1 / 2.5,
    textAllowOverlap: layout['text-allow-overlap'] || false,
    iconAllowOverlap: layout['icon-allow-overlap'] || false,

    iconIgnorePlacement: layout['icon-ignore-placement'] || false,
    iconPadding: layout['icon-padding'] || 2,

    textPadding: layout['text-padding'] || 2,
    textMaxWidth: layout['text-max-width'] || 10,
    textRadialOffset: layout['text-radial-offset'] || 0,

    textFont: layout['text-font'] || [
      'DIN Offc Pro Regular',
      'Arial Unicode MS Regular',
    ],
    textAnchor: layout['text-anchor'] || 'center',
    textVariableAnchor: layout['text-variable-anchor'],
    textField: layout['text-field'] || '', // Mapping text-field for symbols
    textSize: layout['text-size'] || 12, // Mapping text-size
    textColor: paint['text-color'] || '#000', // Mapping text-color
    textHaloColor: paint['text-halo-color'] || '#fff', // Mapping text-halo-color
    textHaloWidth: paint['text-halo-width'] || 0, // Mapping text-halo-width
    textOpacity: paint['text-opacity'] || 1, // Mapping text-opacity
    textTranslate: paint['text-translate'] || [0, 0], // Mapping text-translate
  };
};

